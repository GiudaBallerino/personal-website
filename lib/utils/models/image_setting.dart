class ImageSetting{
  final String path;
  double x;
  double y;
  double width;
  double height;
  double opacity;
  double rotation;

  ImageSetting({
    required this.path,
    required this.x,
    required this.y,
    required this.width,
    required this.height,
    required this.opacity,
    required this.rotation,
  });
}